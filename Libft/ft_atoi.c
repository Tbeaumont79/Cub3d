#include "libft.h"

int		ft_atoi(const char **s1)
{
	unsigned long int	val;

	val = 0;
	while ((**s1 >= 9 && **s1 <= 13) || **s1 == 32)
		(*s1)++;
	while ((ft_isdigit(**s1)) && **s1 != '\0')
		val = val * 10 + ((const char)*((*s1)++) - '0');
	return (val);
}